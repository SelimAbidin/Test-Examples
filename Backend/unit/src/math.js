
/**
 * 
 * @param {number} a 
 * @param {number} b
 * @returns {number} 
 */
function sum(a,b) {
    return a + b
}

/**
 * 
 * @param {number} a 
 * @param {number} b
 * @returns {number} 
 */
function subtract(a,b) {
    return a - b
}

/**
 * 
 * @param {number} a 
 * @param {number} b
 * @returns {number} 
 */
function multiply(a,b) {
    return a * b
}

/**
 * 
 * @param {number} a 
 * @param {number} b
 * @returns {number} 
 */
function divide(a,b) {
    return a / b
}

module.exports = {
    divide,
    multiply,
    subtract,
    sum
}